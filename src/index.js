const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};

function decode(expr) {
    let str="";
    let res="";
    let k=0;
    for(i=0;i<expr.length;i++){
        if(i%10==0 && i!=0 && str!=''){
            res+=MORSE_TABLE[str];
            str="";
        }
       
        if(expr[i]=='1' && expr[i+1]=='0'){
            str+='.';
            i++;
        }
        else
        if(expr[i]=='1' && expr[i+1]=='1'){
            str+='-';
            i++;
        }
        else 
        if(expr[i]=='*' && k==9){
            res+=" ";
            k=0;
        }
        else
        if(expr[i]=='*'){
            k++;
        }

        
    }
    res+=MORSE_TABLE[str];
    return res;
}

module.exports = {
    decode
}